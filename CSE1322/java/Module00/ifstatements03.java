public class ifstatements03 {
    public static void main(String[] args) {
        int grade = 86;

        if(grade >= 90){
            System.out.println("A");
        }
        if(grade >= 80){
            System.out.println("B");
        }
        if(grade >= 70){
            System.out.println("C");
        }
        if(grade >= 60){
            System.out.println("D");
        }
        if(grade >= 50){
            System.out.println("F");
        }
    }
}
